package com.bage.study.springboot.json;

class HelloJsonModel {

    private Long aa = 12300L;
    int a = 30;
    String name = "dsdsd";

    public Long getAa() {
        return aa;
    }

    public void setAa(Long aa) {
        this.aa = aa;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
